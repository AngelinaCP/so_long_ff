#include "libft.h"

int	ft_isalnum(int s)
{
	if ((s >= 'A' && s <= 'Z') || (s >= 'a' && s <= 'z'))
		return (1);
	else if (s >= '0' && s <= '9')
		return (1);
	return (0);
}
